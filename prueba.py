def sumar(a, b):
    """Suma dos números."""
    return a + b

def sumar_varios(*args):
    """Suma varios números."""
    return sum(args)

# Ejemplo de uso
resultado_suma_dos = sumar(5, 3)
print(f"Suma de dos números: {resultado_suma_dos}")  # Imprimirá 8

resultado_suma_varios = sumar_varios(1, 2, 3, 4)
